    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        try {
            CtlCandidato controler = new CtlCandidato();
            int row = this.tbl_Candidatos.getSelectedRow();
            String numeroDoc = this.tbl_Candidatos.getModel().getValueAt(row, 0).toString();

            if (controler.EliminarCandidato(numeroDoc)) {
                JOptionPane.showMessageDialog(this, "El candidato con n√∫mero de c√©dula: " + numeroDoc + " fue eliminado");
                this.ObtenerCandidato();
            } else {
                JOptionPane.showMessageDialog(this, "El candidato con n√∫mero de c√©dula: " + numeroDoc + " no fue eliminado");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

