    public void ActualizarTabla(LinkedList<Candidatos> lista) {

        DefaultTableModel model = (DefaultTableModel) this.tbl_Candidatos.getModel();

        model.setRowCount(0);

        for (Candidatos candidato : lista) {

            Object[] row = {candidato.getNumeroDocumento(), candidato.getNombre(), candidato.getPartidoPolit(), candidato.getDescripcion()};
            model.addRow(row);

        }

    }

