    private void btnVotarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVotarActionPerformed
        // TODO add your handling code here:
        if (this.BuscarVotante(txtDocVotante.getText())) {
            JOptionPane.showMessageDialog(this, "El votante ya realiz√≥ su votaci√≥n. Pruebe otro votante");
        } else {
            try {
                CtlVotos control = new CtlVotos();
                ClsVotos v = new ClsVotos();
                v.setCandidato(cbCandidato.getSelectedItem().toString());
                v.setDocumentoVotante(txtDocVotante.getText());
                if (control.AgregarVotos(v)) {
                    JOptionPane.showMessageDialog(this, "Se Registro Con Exito");
                    this.ObtenerVotos();
                } else {
                    JOptionPane.showMessageDialog(this, "Error al Registarse");
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnVotarActionPerformed

