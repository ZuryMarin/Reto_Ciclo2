    public void ActualizarTabla(LinkedList<Elecciones> lista) {

        DefaultTableModel model = (DefaultTableModel) this.tabla_Eleccion.getModel();

        model.setRowCount(0);

        for (Elecciones eleccion : lista) {

            Object[] row = {eleccion.getDescripcion(), eleccion.getFecha_Inicio(), eleccion.getFecha_Final(), eleccion.getEstado_Eleccion()};
            model.addRow(row);

        }

    }

