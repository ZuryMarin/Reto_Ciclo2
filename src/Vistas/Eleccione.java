    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        try {
            CtlElecciones contr = new CtlElecciones();
            Elecciones e = new Elecciones();
            
            e.setDescripcion(txtNombre.getText());
            e.setFecha_Inicio(txtFecIni.getText());
            e.setFecha_Final(txtFecFin.getText());
            e.setEstado_Eleccion("ACTIVO");
            
            if(contr.CrearEleccion(e)){
                JOptionPane.showMessageDialog(this, "Se Registro Con Exito");
                this.txtNombre.setText("");
                this.txtFecIni.setText("");
                this.txtFecFin.setText("");
                this.ObtenerCandidato();
            }else{
                JOptionPane.showMessageDialog(this, "Error al registrar la Eleccion");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        try {
            CtlElecciones control = new CtlElecciones();
            int row = this.tabla_Eleccion.getSelectedRow();
            String descripcion = this.tabla_Eleccion.getModel().getValueAt(row, 0).toString();
            
            if(control.EliminarEleccion(descripcion)){
                JOptionPane.showMessageDialog(this, "La elección: " + descripcion + " fue eliminada");
                this.ObtenerCandidato();
            }else{
                JOptionPane.showMessageDialog(this, "La elección: " + descripcion + " no pudo ser eliminada");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

